    public function index(Request $request)
    {
        if(!Gate::allows('isAdmin')){
            abort(404, "Forbidden Page.");
        }

        $s = $request->query('s');
        
        $users = DB::table('users')
           ->where('firstName', 'like', "%$s%")
           ->orWhere('lastName', 'like', "%$s%")
           ->get();

        $rRow = 1;

        return view('users.index', compact('users', 'rRow'));
    }

